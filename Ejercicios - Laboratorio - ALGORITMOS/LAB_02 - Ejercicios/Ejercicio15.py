# Pedir al usuario que ingrese una cadena
cadena = input("Ingresa una cadena con espacios en blanco al principio y al final: ")

# Eliminar los espacios en blanco al principio y al final
cadena_sin_espacios = cadena.strip()

# Imprimir la cadena resultante
print("Cadena sin espacios:", cadena_sin_espacios)
