# Encabezados de la tabla
print("Número Potencia 1 Potencia 2 Potencia 3")

# Generar la tabla
for i in range(1, 6):  # Para los números del 1 al 5
    # Calcular las potencias
    potencia_1 = i ** 1
    potencia_2 = i ** 2
    potencia_3 = i ** 3
    
    # Mostrar los resultados en formato de tabla
    print(i, potencia_1, potencia_2, potencia_3)
