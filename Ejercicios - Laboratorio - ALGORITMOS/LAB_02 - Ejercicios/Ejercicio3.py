# Realizar la división entera de 7 entre 3
division_entera = 7 // 3

# Convertir 2.7 a entero
valor_convertido = int(2.7)

# Comprobar si son iguales
if division_entera == valor_convertido:
    print("La división entera de 7 entre 3 es igual a 2.7 convertido a entero.")
else:
    print("La división entera de 7 entre 3 NO es igual a 2.7 convertido a entero.")
