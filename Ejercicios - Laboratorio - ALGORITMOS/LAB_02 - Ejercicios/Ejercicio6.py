# Solicitar al usuario que introduzca las horas trabajadas y la tarifa por hora
horas = float(input("Introduce las horas trabajadas: "))
tarifa_por_hora = float(input("Introduce la tarifa por hora: "))

# Calcular el salario semanal
salario_semanal = horas * tarifa_por_hora

# Mostrar el resultado
print("Tu salario semanal es:", salario_semanal)

